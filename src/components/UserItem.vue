<template>
    <li>
        <label>
            <input type="checkbox" :checked="todo.done" @change="handleCheck(todo.id)"/>
            <span>{{todo.title}}</span>
        </label>
        <button class="btn btn-danger" @click="delTodoItem(todo.id)">删除</button>
    </li>
    
</template>

<script>
export default {
    name:'UserItem',
    props:['todo','checkTodo','delTodo'],
    methods: {
      handleCheck(id){
        this.checkTodo(id)
      },
      delTodoItem(id){
        if(confirm('确认是否删除！')){
          this.delTodo(id)
        }
      }
    },
}
</script>

